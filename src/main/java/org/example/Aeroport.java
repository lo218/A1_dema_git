package org.example;

public class Aeroport {
    String nom;
    String ville;
    String description;

    public Aeroport(String nom) {
        this.nom = nom;
    }

    public String getNom() {
        return nom;
    }

    public String getVille() {
        return ville;
    }

    public String getDescription() {
        return description;
    }
}
